#!/bin/bash

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

echo -e "${YELLOW}Проверка наличия и корректности .env файла...${NC}"

if [ ! -f .env ]; then
    echo -e "${RED}ОШИБКА: Файл .env не найден!${NC}"
    echo ""
    echo -e "${YELLOW}Для работы приложения необходимо:${NC}"
    echo "1. Скопировать файл .env.template в .env:"
    echo "   cp .env.template .env"
    echo "2. Отредактировать .env файл и заменить все значения-заполнители"
    echo "   на реальные значения (пароли, ключи и т.д.)"
    echo "3. Запустить приложение снова"
    echo ""
    echo -e "${YELLOW}ВАЖНО: Не используйте значения по умолчанию в продакшене!${NC}"
    exit 1
fi

